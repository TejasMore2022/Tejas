package javatpointpractice;

public class PeligrimString {

	public static void main(String[] args) {

		
		
		String st= "wow";// string to be checked
		String rst="";
	for(int i=st.length()-1;i>=0;i--) {
		rst=rst+st.charAt(i);
		
		
	}
		
		if(rst.equals(st)) {
			
			System.out.println("I i p");
		}else {
			System.out.println("I i not p");
		}
		

	}

}


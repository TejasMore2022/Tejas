package javatpointpractice;

public class StarPatternAlphabate {

	public static void main(String[] args) {

	
		for(int i=0;i<=4;i++) {
		System.out.print("*");
		}
		System.out.println();
		for(int j=0;j<=1;j++) {
			System.out.println("*   *");
			}
		for(int k=0;k<=4;k++) {
			System.out.print("*");
			}
		System.out.println();
		for(int l=0;l<=2;l++) {
			System.out.println("*   *");
			}

	}

}


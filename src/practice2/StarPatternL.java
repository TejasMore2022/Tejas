public class StarPatternL {

	public static void main(String[] args) {
		int star=3;
		int minus=0;
		
		for(int k=0;k<=star;k++) {
	System.out.print(" ");
	
		star--;
	for(int j=0;j<=star;j++) {
		System.out.print("*");
	for(int i=0;i<=minus;i++) {
			
		System.out.print("-");
	}
	minus++;
	
	System.out.println("*");
	}
	}
			
	}
	}




